ERM_TEMPLATES.oilGas.risks.getAll = function () {
  var all = [];
  var categories = ["wellControl", "processSafety", "environmental", "operational", "drilling", "reserves", "commercial", "cyber", "regulatory"];
  for (var i = 0; i < categories.length; i++) {
    var cat = this[categories[i]];
    if (cat && Array.isArray(cat)) {
      for (var j = 0; j < cat.length; j++) {
        cat[j].riskCategory = categories[i];
        all.push(cat[j]);
      }
    }
  }
  return all;
};

ERM_TEMPLATES.oilGas.risks.findById = function (id) {
  var all = this.getAll();
  for (var i = 0; i < all.length; i++) {
    if (all[i].id === id) return all[i];
  }
  return null;
};

ERM_TEMPLATES.oilGas.risks.search = function (keyword) {
  var results = [];
  var lowerKeyword = keyword.toLowerCase();
  var all = this.getAll();
  for (var i = 0; i < all.length; i++) {
    var risk = all[i];
    var found = false;
    for (var j = 0; j < risk.titles.length; j++) {
      if (risk.titles[j].toLowerCase().indexOf(lowerKeyword) !== -1) { found = true; break; }
    }
    if (!found && risk.keywords) {
      for (var k = 0; k < risk.keywords.length; k++) {
        if (risk.keywords[k].toLowerCase().indexOf(lowerKeyword) !== -1) { found = true; break; }
      }
    }
    if (found) results.push(risk);
  }
  return results;
};

