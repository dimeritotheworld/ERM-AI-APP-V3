  /**
   * Get categories for a specific department
   * @param {string} departmentId - Department ID
   * @returns {Array} Array of categories or empty array
   */
  getByDepartment: function (departmentId) {
    return this[departmentId] || [];
  },

  /**
   * Find a specific category by ID across all departments
   * @param {string} categoryId - Category ID
   * @returns {Object|null} Category object with department info or null
   */
  findById: function (categoryId) {
    var departments = Object.keys(this);
    var i, j, cats;
    for (i = 0; i < departments.length; i++) {
      if (typeof this[departments[i]] === "function") continue;
      cats = this[departments[i]];
      if (!Array.isArray(cats)) continue;
      for (j = 0; j < cats.length; j++) {
        if (cats[j].id === categoryId) {
          return {
            category: cats[j],
            departmentId: departments[i],
          };
        }
      }
    }
    return null;
  },

  /**
   * Get all categories as a flat array
   * @returns {Array} All categories with department info
   */
  getAll: function () {
    var all = [];
    var departments = Object.keys(this);
    var i, j, cats;
    for (i = 0; i < departments.length; i++) {
      if (typeof this[departments[i]] === "function") continue;
      cats = this[departments[i]];
      if (!Array.isArray(cats)) continue;
      for (j = 0; j < cats.length; j++) {
        all.push({
          category: cats[j],
          departmentId: departments[i],
        });
      }
    }
    return all;
  },

  /**
   * Search categories by name or keywords
   * @param {string} searchTerm - Term to search
   * @returns {Array} Matching categories with department info
   */
  search: function (searchTerm) {
    var results = [];
    var lowerTerm = searchTerm.toLowerCase();
    var departments = Object.keys(this);
    var i, j, cats, cat;

    for (i = 0; i < departments.length; i++) {
      if (typeof this[departments[i]] === "function") continue;
      cats = this[departments[i]];
      if (!Array.isArray(cats)) continue;

      for (j = 0; j < cats.length; j++) {
        cat = cats[j];
        if (
          cat.name.toLowerCase().indexOf(lowerTerm) !== -1 ||
          cat.keywords.toLowerCase().indexOf(lowerTerm) !== -1
        ) {
          results.push({
            category: cat,
            departmentId: departments[i],
          });
        }
      }
    }
    return results;
  },

  /**
   * Get department statistics
   * @returns {Object} Statistics about categories per department
   */
  getStats: function () {
    var stats = {
      totalCategories: 0,
      totalDepartments: 0,
      byDepartment: {},
    };
    var departments = Object.keys(this);
    var i, cats;

    for (i = 0; i < departments.length; i++) {
      if (typeof this[departments[i]] === "function") continue;
      cats = this[departments[i]];
      if (!Array.isArray(cats)) continue;

      stats.totalDepartments++;
      stats.totalCategories += cats.length;
      stats.byDepartment[departments[i]] = cats.length;
    }
    return stats;
  },
